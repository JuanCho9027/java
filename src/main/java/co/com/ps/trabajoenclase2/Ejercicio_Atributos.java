package co.com.ps.trabajoenclase2;

public class Ejercicio_Atributos {
    String marca;
    String color;
    String transmision;
    String carroceria;
    int llantas;
    int precio;
    boolean electrico;

    public Ejercicio_Atributos(String marca, String color, String transmision, String carroceria, int llantas, int precio, boolean electrico) {
        this.marca = marca;
        this.color = color;
        this.transmision = transmision;
        this.carroceria = carroceria;
        this.llantas = llantas;
        this.precio = precio;
        this.electrico = electrico;
    }
}
